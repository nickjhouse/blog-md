// Build-time extraction of brand-icon SVG paths from `simple-icons` into a local
// constants file (src/lib/social-icons.ts). This keeps the icons accurate and
// maintained WITHOUT importing simple-icons at runtime — nothing from the package
// ships in the client or worker bundle, so there's no tree-shaking risk.
//
// Re-run after bumping simple-icons (or to add an icon): npm run gen:icons
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import {
  siReddit,
  siX,
  siFacebook,
  siBluesky,
  siGithub,
  siMastodon,
  siLinkedin,
} from "simple-icons";

const ICONS = {
  reddit: siReddit,
  x: siX,
  facebook: siFacebook,
  bluesky: siBluesky,
  github: siGithub,
  mastodon: siMastodon,
  linkedin: siLinkedin,
};

const body = Object.entries(ICONS)
  .map(([key, icon]) => `  ${key}: ${JSON.stringify(icon.path)},`)
  .join("\n");

const out = `// AUTO-GENERATED by scripts/generate-social-icons.mjs — do not edit by hand.
// Brand glyph paths extracted from the simple-icons package at build time, so
// nothing from that package ships at runtime. Re-run: npm run gen:icons
export const SOCIAL_ICON_PATHS = {
${body}
} as const;
`;

const target = fileURLToPath(new URL("../src/lib/social-icons.ts", import.meta.url));
writeFileSync(target, out);
console.log(`Wrote ${target}`);
